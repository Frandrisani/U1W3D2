/* ESERCIZIO 5 (reduce)
  Scrivi una funzione per sommare i numeri contenuti in un array
*/
const numArrayReduce = (array) => {
  const sum = array.reduce((acc, current) => {
    return acc + current;
  }, 0);

  return sum;
};
console.log(numArrayReduce([26, 478, 67, 54, 36, 73]));

/* ESERCIZIO 6 (map)
  Scrivi una funzione che, dato un array di soli numeri e un numero n come parametri, ritorni un secondo array con tutti i valori del precedente incrementati di n
*/
const arraySoliNumeriIncrementati = (array, n) => {
  const arraySoliNumeri = array.map((elemento) => {
    return elemento + n;
  });
  return arraySoliNumeri;
};
console.log(arraySoliNumeriIncrementati([1, 4, 6], 1));

