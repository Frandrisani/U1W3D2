/* ESERCIZIO 8 (forEach o for)
  Scrivi una funzione per creare un array contenente tutti i valori DISPARI da 1 a 99.
*/
const arrayDispari = (array) => {
  let numDisp = [];
  array.forEach((num) => {
    if (num >= 1 && num <= 99) {
      if (num % 2 !== 0) {
        return numDisp.push(num);
      }
    }
  });
  return numDisp;
};
console.log(arrayDispari([6, 5, 12, 37, 17, 113, 237]));

