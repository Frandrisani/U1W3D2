/* ESERCIZIO 3 (filter)
  Scrivi una funzione per ricavare solamente i valori PARI da un array composto da soli valori numerici
*/
const numPar = (arr) => {
  const num = arr.filter((elemento) => {
    return elemento % 2 === 0;
  });
  return num;
};
console.log(numPar([2, 4, 7, 5, 3, 7]));

